const month = "march";

switch (month) {
    case "jan":
        console.log("January");
        break;
    case "feb":
        console.log("February");
        break;
    
    case "april":
        console.log("March");
        break;
    
    case "march":
        console.log("April");
        break;
    
         default:
        console.log("Default");
        
        break;
}